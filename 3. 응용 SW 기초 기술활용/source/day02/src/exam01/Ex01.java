package exam01;

public class Ex01 {
    static int  num1; // 기본값 넣으면 0이다
    static double num2;  //기본값 넣으면 0.0이다
    static boolean bool1; // 기본값 false
    static String str;

    public static void main(String[] args) {
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(bool1);
        System.out.println(str);
    }
}
